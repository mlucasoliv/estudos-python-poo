"""
Python = Linguagem de Programação
TIpo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas Simples
print('Luiz Otávio')
print('"Luiz Otávio"')

# Aspas Duplas
print("Luiz Otávio")
print("'Luiz Otávio'")

# Escape
print("Luiz \"Otávio")

# r
print(r"Luiz \"Otávio")